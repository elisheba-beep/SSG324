inputNumber = int(input("Please input a number from 1 to 7:"))
if inputNumber == 1:
    print("Sunday")
elif inputNumber == 2:
    print("Monday")
elif inputNumber == 3:
    print("Tuesday")
elif inputNumber == 4:
    print("Wednesday")
elif inputNumber == 5:
    print("Thursday")
elif inputNumber == 6:
    print("Friday")
elif inputNumber == 7:
    print("Saturday")
else:
    print("Invalid number")